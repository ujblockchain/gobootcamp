package main

import "fmt"

func main() {
	// a variable is a placeholder, a container to hold something
	// "var" is used to declare variable

	// declaring a byte variable
	var vowel byte = 'a'

	//printing byte storage number
	fmt.Println(vowel)

	//printing byte value
	fmt.Println(string(vowel))

	//more example
	var favoriteLetter byte = 'I'

	//printing byte storage number
	fmt.Println(favoriteLetter)

	//printing byte value
	fmt.Println(string(favoriteLetter))

}
