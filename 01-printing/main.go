package main

import "fmt"

func main() {
	// using fmt Print,
	// Print does not add a new line
	fmt.Print("Hello World")
	fmt.Print("Welcome")

	// using print with "\n"
	// this puts each in a new line
	fmt.Print("Hello World \n")
	fmt.Print("Welcome \n")

	//using fmt Println
	// this puts it in a new line
	fmt.Println("Hello World")

	// print your name
	fmt.Println("My name is John Deo")

	//printing your department
	fmt.Println("My department is Electrical & Electronic Engineering")
}
